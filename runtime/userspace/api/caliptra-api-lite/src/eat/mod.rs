//! EAT claims byte-template — pre-computed CBOR with fill slots.
//!
//! The entire EAT payload (including the embedded ConciseEvidence) is a
//! fixed 206-byte CBOR encoding with exactly two variable regions:
//!   - nonce  (32 bytes at offset 4)
//!   - digest (48 bytes at offset 120)
//!
//! At runtime we `copy_from_slice` the template, then patch the two slots.
//! This eliminates all CborEncoder function calls from the measurement path.

/// Size of the complete EAT claims payload (fixed for single platform-state component).
pub const EAT_PAYLOAD_LEN: usize = 206;

/// Offset of the 32-byte nonce within the template.
const NONCE_OFFSET: usize = 4;

/// Offset of the 48-byte SHA-384 digest within the template.
const DIGEST_OFFSET: usize = 120;

/// Pre-computed EAT payload CBOR with zero-filled nonce and digest slots.
