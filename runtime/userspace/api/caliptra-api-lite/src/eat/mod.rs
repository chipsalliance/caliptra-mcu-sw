/// Stamp the EAT payload template into `buf`, filling nonce and digest.
///
/// Returns the payload length (always [`EAT_PAYLOAD_LEN`]).
/// Returns `None` if `buf` is too small.
pub fn stamp_eat_payload(
    buf: &mut [u8],
    nonce: &[u8; 32],
    digest: &[u8; 48],
) -> Option<usize> {
    if buf.len() < EAT_PAYLOAD_LEN {
        return None;
    }
    buf[..EAT_PAYLOAD_LEN].copy_from_slice(&EAT_PAYLOAD_TEMPLATE);
    buf[NONCE_OFFSET..NONCE_OFFSET + 32].copy_from_slice(nonce);
    buf[DIGEST_OFFSET..DIGEST_OFFSET + 48].copy_from_slice(digest);
    Some(EAT_PAYLOAD_LEN)
}
